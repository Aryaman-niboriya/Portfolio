interface BinaryTextCanvasProps {
  text: string;
  pixelSize?: number;         // size of each "dot" pixel in px
  color?: string;
  dimColor?: string;
  gap?: number;               // gap between pixels
  letterSpacing?: number;     // gap between letters
  showZeros?: boolean;        // whether to render 0s in the off-pixels
  className?: string;
}

export const BinaryTextCanvas: React.FC<BinaryTextCanvasProps> = ({
  text,
  pixelSize = 4,
  color = "#00ff41",
  dimColor = "rgba(0,255,65,0.04)",
  gap = 1,
  letterSpacing = 2,
  showZeros = true,
  className = "",
}) => {
  const canvasRef = useRef<HTMLCanvasElement>(null);

  const chars = useMemo(() => text.toUpperCase().split(""), [text]);

  // Calculate canvas dimensions
  const CHAR_W = 5;
  const CHAR_H = 7;
  const step = pixelSize + gap;
  const charWidth = CHAR_W * step + letterSpacing * step;
  const totalWidth = chars.length * charWidth - letterSpacing * step;
  const totalHeight = CHAR_H * step;

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    ctx.clearRect(0, 0, canvas.width, canvas.height);

    chars.forEach((ch, ci) => {
      const grid = PIXEL_FONT[ch] || PIXEL_FONT[" "];
      const offsetX = ci * charWidth;

      grid.forEach((row, ri) => {
        row.forEach((on, pi) => {
          const x = offsetX + pi * step;
          const y = ri * step;

          ctx.fillStyle = on ? color : (showZeros ? dimColor : "transparent");
          ctx.fillRect(x, y, pixelSize, pixelSize);

          // Draw the 1 or 0 text inside pixel if large enough
          if (pixelSize >= 6) {
            ctx.font = `${pixelSize - 2}px monospace`;
            ctx.fillStyle = on ? "#010602" : (showZeros ? "rgba(0,255,65,0.08)" : "transparent");
            ctx.textAlign = "center";
            ctx.textBaseline = "middle";
            ctx.fillText(on ? "1" : "0", x + pixelSize / 2, y + pixelSize / 2);
          }
        });
      });
    });
  }, [chars, pixelSize, color, dimColor, gap, letterSpacing, showZeros, charWidth, step]);

  return (
    <canvas
      ref={canvasRef}
      width={Math.max(1, totalWidth)}
      height={totalHeight}
      className={`block max-w-full h-auto ${className}`}
      style={{ imageRendering: "pixelated" }}
    />
  );
};
